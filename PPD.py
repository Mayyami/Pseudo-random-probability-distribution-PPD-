# Импортируем функцию find_c из модуля C
from C import find_c
# Импортируем модуль random для генерации случайных чисел
import random

# Объявляем функцию pseudo_random, которая принимает максимальное количество попыток (max_try) и количество повторений (retry)
def pseudo_random(max_try, retry):
    # Находим начальное значение константы 'c' с помощью функции find_c, передавая ей max_try
    c0 = find_c(max_try)
    # Выводим найденное значение константы 'c'
    print("Константа 'c' для данного шанса составляет:", c0)
    # Инициализируем текущее значение 'c' начальным значением c0
    c = c0
    # Инициализируем счетчик текущих попыток до гарантированного срабатывания
    now_retry = 1
    # Инициализируем счетчик успешных попыток
    success_try = 0
    # Начинаем цикл, который будет выполняться 'retry' раз
    for i in range(retry):
        # Генерируем случайное число с плавающей точкой в диапазоне от 0 до 1
        random_number = random.uniform(0, 1)
        # Выводим разделитель для наглядности
        print("-------------------------------")
        # Выводим сообщение о начале проверки
        print("YEEES or no?...")
        # Проверяем, меньше или равно ли сгенерированное случайное число текущему значению 'c'
        if random_number <= c:
            # Если условие истинно (успешная попытка)
            print("YEEES")
            # Сбрасываем значение 'c' до начального c0
            c = c0
            # Сбрасываем счетчик текущих попыток до 1
            now_retry = 1
            # Увеличиваем счетчик успешных попыток на 1
            success_try += 1
            # Выводим разделитель
            print("-------------------------------")
        else:
            # Если условие ложно (неудачная попытка)
            print("no...")
            # Увеличиваем значение 'c' на c0 (увеличиваем шанс на следующей попытке)
            c += c0
            # Увеличиваем счетчик текущих попыток на 1
            now_retry += 1
            # Выводим количество оставшихся попыток до гарантированного срабатывания
            print(f"Попыток до гаранта: {(max_try - now_retry) + 1}")
            # Выводим разделитель
            print("-------------------------------")
    # Вычисляем общий шанс на победу как отношение успешных попыток к общему количеству повторений
    win_rate = success_try / retry
    # Возвращаем количество успешных попыток и средний шанс на победу
    return success_try, win_rate

# Инициализируем переменную s значением 1 для запуска основного цикла программы
s = 1
# Начинаем основной цикл программы, который будет продолжаться, пока s равно 1
while s == 1:
    # Устанавливаем s в 1, чтобы цикл гарантированно продолжился хотя бы один раз
    s = 1
    # Запрашиваем у пользователя ввод максимального количества попыток для гарантированного срабатывания
    n = int(input("Введите максимальное количество попыток для гарантированного срабатывания:"))
    # Проверяем, является ли введенное значение n меньше или равным 0
    if n <= 0:
        # Если условие истинно, выводим сообщение об ошибке
        print("Количество попыток до гаранта должно быть больше нуля")
    # Запрашиваем у пользователя ввод количества повторений
    retry = int(input("Введите количество повторений:"))
    # Проверяем, является ли введенное значение retry меньше или равным 0
    if retry <= 0:
        # Если условие истинно, выводим сообщение об ошибке
        print("Количество попыток должно быть больше нуля")
    else:
        # Если оба предыдущих условия ложны, вызываем функцию pseudo_random с введенными значениями n и retry
        s = pseudo_random(n, retry)
        # Выводим количество удачных попыток, полученное от функции pseudo_random
        print("Количество удачных попыток:", s[0])
        # Выводим средний шанс на победу, полученный от функции pseudo_random
        print("Средний шанс на победу:", s[1])
    # Запрашиваем у пользователя ввод для продолжения или завершения программы
    s = int(input("Нажмите 1 чтобы начать заново или 0 чтобы закончить программу:"))